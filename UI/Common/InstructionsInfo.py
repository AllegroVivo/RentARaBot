################################################################################

__all__ = ("InstructionsInfo",)

################################################################################
class InstructionsInfo:
    
    __slots__ = (
        "_placeholder",
        "_value",
    )

################################################################################
    def __init__(self, placeholder: str, value: str) -> None:

        self._placeholder: str = placeholder
        self._value: str = value
        
################################################################################
    @property
    def placeholder(self) -> str:
        
        return self._placeholder
    
################################################################################
    @property
    def value(self) -> str:
        
        return self._value
    
################################################################################
